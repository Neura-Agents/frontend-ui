import * as React from "react"
import { Command as CommandPrimitive } from "cmdk"

import { cn } from "@/lib/utils"
import {
  Dialog,
  DialogContent,
  DialogDescription,
  DialogHeader,
  DialogTitle,
} from "@/components/ui/dialog"

function Command({
  className,
  ...props
}: React.ComponentProps<typeof CommandPrimitive>) {
  return (
    <CommandPrimitive
      data-slot="command"
      className={cn(
        "flex size-full flex-col overflow-hidden p-2 text-foreground rounded-4xl border border-border bg-card/50",
        className
      )}
      {...props}
    />
  )
}

function CommandDialog({
  title = "Command Palette",
  description = "Search for a command to run...",
  children,
  className,
  showCloseButton = false,
  shouldFilter,
  ...props
}: React.ComponentProps<typeof Dialog> & {
  title?: string
  description?: string
  className?: string
  showCloseButton?: boolean
  shouldFilter?: boolean
}) {
  return (
    <Dialog {...props}>
      <DialogHeader className="sr-only">
        <DialogTitle>{title}</DialogTitle>
        <DialogDescription>{description}</DialogDescription>
      </DialogHeader>
      <DialogContent
        className={cn(
          "max-sm:h-dvh max-sm:max-h-dvh max-sm:top-0 max-sm:rounded-none top-1/3 translate-y-0 overflow-hidden rounded-4xl! p-0 sm:max-w-2xl w-full my-4",
          !showCloseButton && "sm:**:data-[slot=dialog-close]:hidden",
          className
        )}
        showCloseButton={true}
      >
        <Command 
          shouldFilter={shouldFilter}
          className="**:[[cmdk-group-heading]]:px-2 **:[[cmdk-group-heading]]:font-medium **:[[cmdk-group-heading]]:text-muted-foreground **:[[cmdk-group]]:px-2 **:[[cmdk-input-wrapper]_svg]:h-5 **:[[cmdk-input-wrapper]_svg]:w-5 **:[[cmdk-input]]:h-12 **:[[cmdk-item]]:px-3 **:[[cmdk-item]]:py-2 **:[[cmdk-item]_svg]:h-5 **:[[cmdk-item]_svg]:w-5 border-none bg-transparent"
        >
          {children}
        </Command>
      </DialogContent>
    </Dialog>
  )
}

